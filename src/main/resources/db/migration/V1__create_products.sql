CREATE TABLE products (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(255) NOT NULL,
    seller_name VARCHAR(255) NOT NULL,
    description VARCHAR(2000) NOT NULL,
    product_type VARCHAR(30) NOT NULL,
    product_status VARCHAR(30) NOT NULL,
    original_price INTEGER NOT NULL,
    start_price INTEGER NOT NULL,
    current_price INTEGER NOT NULL,
    min_price INTEGER NOT NULL,
    max_discount_rate INTEGER NOT NULL,
    discount_step_participants INTEGER NOT NULL,
    discount_step_amount INTEGER NOT NULL,
    min_participants INTEGER NOT NULL,
    current_participants INTEGER NOT NULL,
    max_participants INTEGER NOT NULL,
    remaining_stock INTEGER NOT NULL,
    shipping_fee INTEGER,
    end_at TIMESTAMP NOT NULL,
    coupon_event BOOLEAN NOT NULL,
    coupon_rate INTEGER,
    visual VARCHAR(100),
    icon VARCHAR(100),
    rating DOUBLE PRECISION NOT NULL,
    review_count INTEGER NOT NULL,
    giveaway_reason VARCHAR(500),
    giveaway_promotional_purpose BOOLEAN,
    giveaway_fulfillment_methods VARCHAR(100),
    giveaway_shipping_fee INTEGER,
    pickup_store_name VARCHAR(255),
    pickup_address VARCHAR(500),
    pickup_instructions VARCHAR(1000),
    pickup_deposit INTEGER
);
